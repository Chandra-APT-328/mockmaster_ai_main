            .then((phone_codes) => {
                let codes = [];
                phone_codes.forEach(phoneCode => {
                    if(phoneCode.country == "Australia"){
                        codes.push({id: phoneCode.code, text: `${phoneCode.country} (${phoneCode.code})`, selected: true})
                    }else{
                        codes.push({id: phoneCode.code, text: `${phoneCode.country} (${phoneCode.code})`, selected: false})
                    }
                });
                $("#countrycode").html('').select2({data: codes}); 
            })
